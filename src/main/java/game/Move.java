package game;

/**
 * Egy {@link Snake} lehetséges fordulási szándékait tartalmazó enum.
 */
public enum Move {
	/**
	 * Balra szeretné folytatni az útját.
	 */
	LEFT,
    /**
     * Jobbra szeretné folytatni az útját.
     */
	RIGHT,
	/**
	 * Egyenesen szeretné folytatni az útját.
	 */
	FORWARD
	 
}
